/// Computes the distance between the arguments.
///
/// - Parameters:
///   - x: a float argument.
///   - y: a float argument.
/// - Returns: the distance between the arguments.
public func distance(_ x: Float32, _ y: Float32) -> Float32 { abs(x - y) }

/// Computes the distance between the arguments.
///
/// - Parameters:
///   - x: a double argument.
///   - y: a double argument.
/// - Returns: the distance between the arguments.
public func distance(_ x: Float64, _ y: Float64) -> Float64 { abs(x - y) }
